package com.example.easyticket;

public class MovieDetails {
    private int name;
    private int date;
    private  int time;
    private int movieName;

    public MovieDetails() {
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getMovieName() {
        return movieName;
    }

    public void setMovieName(int movieName) {
        this.movieName = movieName;
    }
}
